def wada_snr(wav_path: Path) -> float:
    data, sr = sf.read(str(wav_path), dtype="float32")
    if data.ndim > 1:
        data = data.mean(axis=1)
    abs_data = np.abs(data)
    abs_data = abs_data[abs_data > 1e-6]
    if abs_data.size == 0:
        return 0.0
    v1 = float(np.mean(abs_data))
    v2 = float(np.mean(np.log(abs_data))) - float(np.log(v1))
    wada_g = float(np.exp(v2))
    idx = int(np.argmin(np.abs(_G_VALS - wada_g)))
    return float(_DB_VALS[idx])
